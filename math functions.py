import math
# ciò ha importato la matematica

numero = -8.17
print(round(numero))
# arrotonda il numero
print(math.ceil(numero))
# porta il numero al numero piu alto successivo intero
print(math.floor(numero))
# abbassa il numero fino al prossimo numero intero
print(math.fabs(numero))
# da il valore assoluto del numero
print(pow(numero,2))
# fa la potenza del numero
print(math.sqrt(numero*-1))
# ho dovuto fare *-1 poichè non ci sono radici quadrate di numeri negativi
